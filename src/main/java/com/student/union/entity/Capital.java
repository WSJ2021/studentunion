package com.student.union.entity;

public class Capital {
    private String capitalId;
    private String capitalPerson;
    private String capitalDetail;
    private String capitalTime;
    private String capitalDepart;
    private String capitalMoney;
    private String capitalState;
    private String capitalActName;

    public String getCapitalActName() {
        return capitalActName;
    }

    public void setCapitalActName(String capitalActName) {
        this.capitalActName = capitalActName;
    }

    public String getCapitalId() {
        return capitalId;
    }

    public void setCapitalId(String capitalId) {
        this.capitalId = capitalId;
    }

    public String getCapitalPerson() {
        return capitalPerson;
    }

    public void setCapitalPerson(String capitalPerson) {
        this.capitalPerson = capitalPerson;
    }

    public String getCapitalDetail() {
        return capitalDetail;
    }

    public void setCapitalDetail(String capitalDetail) {
        this.capitalDetail = capitalDetail;
    }

    public String getCapitalTime() {
        return capitalTime;
    }

    public void setCapitalTime(String capitalTime) {
        this.capitalTime = capitalTime;
    }

    public String getCapitalDepart() {
        return capitalDepart;
    }

    public void setCapitalDepart(String capitalDepart) {
        this.capitalDepart = capitalDepart;
    }

    public String getCapitalMoney() {
        return capitalMoney;
    }

    public void setCapitalMoney(String capitalMoney) {
        this.capitalMoney = capitalMoney;
    }

    public String getCapitalState() {
        return capitalState;
    }

    public void setCapitalState(String capitalState) {
        this.capitalState = capitalState;
    }
}
