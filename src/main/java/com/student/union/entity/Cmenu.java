package com.student.union.entity;

public class Cmenu {
    String cmenuId;
    String cmenuName;
    String menuId;
    String paths;
    public String getPaths() {
        return paths;
    }

    public void setPaths(String paths) {
        this.paths = paths;
    }



    public String getMenuId() {
        return menuId;
    }

    public void setMenuId(String menuId) {
        this.menuId = menuId;
    }

    public String getCmenuId() {
        return cmenuId;
    }

    public void setCmenuId(String cmenuId) {
        this.cmenuId = cmenuId;
    }

    public String getCmenuName() {
        return cmenuName;
    }

    public void setCmenuName(String cmenuName) {
        this.cmenuName = cmenuName;
    }
}
