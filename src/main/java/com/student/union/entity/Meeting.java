package com.student.union.entity;

public class Meeting {
    private String meetingId;
    private String meetingTime;
    private String meetingContent;
    private String meetingUser;
    private String meetingTitle;
    private String meetingPlace;
    private String meetingHost;
    private String meetingCapital;
    public String getMeetingCapital() {
        return meetingCapital;
    }
    public void setMeetingCapital(String meetingCapital) {
        this.meetingCapital = meetingCapital;
    }
    public String getMeetingId() {
        return meetingId;
    }

    public void setMeetingId(String meetingId) {
        this.meetingId = meetingId;
    }

    public String getMeetingTime() {
        return meetingTime;
    }

    public void setMeetingTime(String meetingTime) {
        this.meetingTime = meetingTime;
    }

    public String getMeetingContent() {
        return meetingContent;
    }

    public void setMeetingContent(String meetingContent) {
        this.meetingContent = meetingContent;
    }

    public String getMeetingUser() {
        return meetingUser;
    }

    public void setMeetingUser(String meetingUser) {
        this.meetingUser = meetingUser;
    }

    public String getMeetingTitle() {
        return meetingTitle;
    }

    public void setMeetingTitle(String meetingTitle) {
        this.meetingTitle = meetingTitle;
    }

    public String getMeetingPlace() {
        return meetingPlace;
    }

    public void setMeetingPlace(String meetingPlace) {
        this.meetingPlace = meetingPlace;
    }

    public String getMeetingHost() {
        return meetingHost;
    }

    public void setMeetingHost(String meetingHost) {
        this.meetingHost = meetingHost;
    }
}
